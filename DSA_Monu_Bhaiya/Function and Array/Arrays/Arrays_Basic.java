package Arrays;

public class Arrays_Basic {
    public static void main(String[] args) {
        int [] arr = new int[5];
        System.out.println(arr);
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        int [] ar = {1,2,3,4,5}; 
        for (int i = 0; i < ar.length; i++) {
            System.out.print(i + " ");
        }
    }
}
